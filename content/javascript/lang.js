/* Aplikacja tłumaczeń */
function applyLang(lang){
  const t = dict[lang] || dict.pl;

  // html lang + meta description
  document.documentElement.setAttribute('lang', t["html.lang"]);
  const metaDesc = document.querySelector('meta[name="description"]');
  if (metaDesc) metaDesc.setAttribute('content', t["meta.desc"]);

  // wypełnij elementy data-i18n
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const val = t[key];
    if (typeof val === 'string') {
      el.innerHTML = val;
    }
  });

  // zapamiętaj i zaktualizuj stany przycisków języka (jeśli istnieją)
  localStorage.setItem('lang', lang);
  const btnPl = document.getElementById('btn-pl');
  const btnEn = document.getElementById('btn-en');
  if (btnPl) btnPl.setAttribute('aria-pressed', lang === 'pl' ? 'true' : 'false');
  if (btnEn) btnEn.setAttribute('aria-pressed', lang === 'en' ? 'true' : 'false');
  
  
  // 3a) wstaw tłumaczone sufiksy do liczników
document.querySelectorAll('[data-suffix-i18n]').forEach(el => {
  const key = el.getAttribute('data-suffix-i18n');
  const val = t[key];
  if (typeof val === 'string') {
    el.dataset.suffix = val;             // <-- licznik użyje tego przy animacji
  }
});

// 3b) jeśli licznik już wyświetla liczbę, odśwież widoczny sufiks bez ponownej animacji
document.querySelectorAll('.num[data-suffix]').forEach(el => {
  const m = (el.textContent || '').match(/\d+/); // złap aktualną liczbę
  if (m) el.textContent = m[0] + (el.dataset.suffix || '');
});

}

// inicjalizacja + obsługa kliknięć
const savedLang = localStorage.getItem('lang') || 'pl';
applyLang(savedLang);
const _btnPl = document.getElementById('btn-pl');
const _btnEn = document.getElementById('btn-en');
if (_btnPl) _btnPl.addEventListener('click', () => applyLang('pl'));
if (_btnEn) _btnEn.addEventListener('click', () => applyLang('en'));